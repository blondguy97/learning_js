console.log(2 * 4 == 8); /* 
== это оператор равенства,
(один знак равно =, это присваивание, а два знака равно это уже знак равенства) 
вызвращает true или false. 2 умножить на 4 будет 8. Возвращает true */

console.log(2 * 4 == '8'); // Тоже true, так как это оператор НЕ строго равенства, оно сравнивает по значению, а не по типу данных. Ведь тип данных 8ми это строка. Но JavaScript сам преобразует 8 к числу чтобы было легче.

console.log(2 * 4 === '8'); // === это уже оператор строгого равенства, вызвращает false. Он уже смотрит именно типы данных, а тут очевидно что слева числа, а справа строка. Они не равны

console.log(2 + 2 == 4 && 2 - 2 == 0); // Оператор && (И) возвращает true, когда оба или более выражений верны, но если хоть одно ложно, возвращает false

console.log(2 + 5 == 7 || 5 + 5 == 9); // Оператор || (ИЛИ) возвращает true, когда хоть одно из выражений верно, но если уже все ложны, возвращает false

console.log(!true); // false. воскл. знак это оператор отрицания (не равно), если было false то он обратит в true и наоборот

